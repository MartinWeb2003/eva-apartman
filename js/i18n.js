  /* ── Helpers ─────────────────────────────────────────── */
  var LANGS = ['en', 'hr', 'pl', 'de'];

  function getLang() {
    var stored = localStorage.getItem(LANG_KEY);
    return (stored && LANGS.indexOf(stored) !== -1) ? stored : 'en';
  }

  function setLang(lang) {
    localStorage.setItem(LANG_KEY, lang);
  }

  function detectPage() {
    var p = window.location.pathname.toLowerCase();
    if (p.indexOf('gallery')  !== -1) return 'gallery';
    if (p.indexOf('location') !== -1) return 'location';
    if (p.indexOf('contact')  !== -1) return 'contact';
    return 'index';
  }

  /* ── Apply translations ──────────────────────────────── */
  function applyLang(lang) {
    var t    = T[lang] || T.en;
    var page = detectPage();

    /* html[lang] + class */
    document.documentElement.lang = lang;
    document.documentElement.classList.remove('lang-en', 'lang-hr', 'lang-pl', 'lang-de');
    document.documentElement.classList.add('lang-' + lang);

    /* document.title */
    var titleKey = page + '.title';
    if (t[titleKey]) document.title = t[titleKey];

    /* [data-i18n] → textContent */
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (t[key] !== undefined) el.textContent = t[key];
    });

    /* [data-i18n-html] → innerHTML */
    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-html');
      if (t[key] !== undefined) el.innerHTML = t[key];
    });

    /* [data-i18n-placeholder] → placeholder */
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-placeholder');
      if (t[key] !== undefined) el.placeholder = t[key];
    });

    /* Update dropdown: button label + active option */
    var menuBtn = document.getElementById('lang-toggle');
    if (menuBtn) {
      var current = menuBtn.querySelector('.lang-current');
      if (current) current.textContent = lang.toUpperCase();
      document.querySelectorAll('.lang-option').forEach(function (opt) {
        opt.classList.toggle('active', opt.dataset.lang === lang);
      });
    }

    /* Notify other components (e.g. calendar) that language changed */
    document.dispatchEvent(new CustomEvent('eva:langchange', { detail: { lang: lang } }));
  }

  /* ── Dropdown logic ──────────────────────────────────── */
  function initDropdown() {
    var menuBtn = document.getElementById('lang-toggle');
    if (!menuBtn) return;
    var wrap = menuBtn.closest('.lang-menu-wrap');
    if (!wrap) return;

    /* Toggle open / close */
    menuBtn.addEventListener('click', function (e) {
      e.stopPropagation();
      var isOpen = wrap.classList.toggle('open');
      menuBtn.setAttribute('aria-expanded', isOpen ? 'true' : 'false');
    });

    /* Option clicks */
    wrap.querySelectorAll('.lang-option').forEach(function (opt) {
      opt.addEventListener('click', function (e) {
        e.stopPropagation();
        var lang = opt.dataset.lang;
        if (lang) { setLang(lang); applyLang(lang); }
        wrap.classList.remove('open');
        menuBtn.setAttribute('aria-expanded', 'false');
      });
    });

    /* Close on outside click */
    document.addEventListener('click', function () {
      wrap.classList.remove('open');
      menuBtn.setAttribute('aria-expanded', 'false');
    });

    /* Close on Escape */
    document.addEventListener('keydown', function (e) {
      if (e.key === 'Escape') {
        wrap.classList.remove('open');
        menuBtn.setAttribute('aria-expanded', 'false');
      }
    });
  }

  /* ── Init ────────────────────────────────────────────── */
  document.addEventListener('DOMContentLoaded', function () {
    initDropdown();
    applyLang(getLang());
  });

